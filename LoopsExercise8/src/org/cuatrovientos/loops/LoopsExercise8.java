/**
 * @author julen
 *
 */
public class LoopsExercise8 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		for (int i = 0; i < 11; i++) {
			for (int j = 0; j < 11; j++) {
				System.out.print(i + "x" + j + "=" + i * j + " ");
			}
			System.out.println("");
		}

	}

}
